export function searchCities(query: string, limit = 8): PolishCity[] {
  if (!query || query.length < 2) return [];
  const q = query.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
  return POLISH_CITIES.filter(city => {
    const name = city.name.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
    return name.startsWith(q) || name.includes(q);
  }).slice(0, limit);
}

export function findCity(name: string): PolishCity | undefined {
  const q = name.toLowerCase().trim();
  return POLISH_CITIES.find(city => city.name.toLowerCase() === q);
}
